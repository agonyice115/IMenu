package com.huassit.imenu.android.model;

public class MenuCategory {

	private String menu_category_id;
	private String menu_category_name;
	private String parent_id;
	private String menu_category_image_name;
	private String menu_category_image_location;
	private String menu_category_image_date;
	private String sort_order;
	private String menu_ids;
	public String getMenu_category_id() {
		return menu_category_id;
	}
	public void setMenu_category_id(String menu_category_id) {
		this.menu_category_id = menu_category_id;
	}
	public String getMenu_category_name() {
		return menu_category_name;
	}
	public void setMenu_category_name(String menu_category_name) {
		this.menu_category_name = menu_category_name;
	}
	public String getParent_id() {
		return parent_id;
	}
	public void setParent_id(String parent_id) {
		this.parent_id = parent_id;
	}
	public String getMenu_category_image_name() {
		return menu_category_image_name;
	}
	public void setMenu_category_image_name(String menu_category_image_name) {
		this.menu_category_image_name = menu_category_image_name;
	}
	public String getMenu_category_image_location() {
		return menu_category_image_location;
	}
	public void setMenu_category_image_location(String menu_category_image_location) {
		this.menu_category_image_location = menu_category_image_location;
	}
	public String getMenu_category_image_date() {
		return menu_category_image_date;
	}
	public void setMenu_category_image_date(String menu_category_image_date) {
		this.menu_category_image_date = menu_category_image_date;
	}
	public String getSort_order() {
		return sort_order;
	}
	public void setSort_order(String sort_order) {
		this.sort_order = sort_order;
	}
	public String getMenu_ids() {
		return menu_ids;
	}
	public void setMenu_ids(String menu_ids) {
		this.menu_ids = menu_ids;
	}
	

}
